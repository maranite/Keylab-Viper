/**
 * An enum defining the note expressions available in Bitwig Studio, used for the expression parameter of
 * {#assignPolyphonicAftertouchToExpression}.
 */
declare enum NoteExpression {
    NONE = 0,
    PITCH_DOWN = 1,
    PITCH_UP = 2,
    GAIN_DOWN = 3,
    GAIN_UP = 4,
    PAN_LEFT = 5,
    PAN_RIGHT = 6,
    TIMBRE_DOWN = 7,
    TIMBRE_UP = 8
}
